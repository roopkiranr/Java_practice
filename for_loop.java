package introduction;

public class for_loop {

	public static void main(String[] args) {
	
		int a =10;
		switch(a)
		{
		case 1:
			System.out.println("case 1");
			break;
		case 2:
			System.out.println("case 2");
			break;
		case 3:
			System.out.println("case 3");
			break;
			default:
				System.out.println("invalid input");
		}
	}

}
